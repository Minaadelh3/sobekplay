// --- HELPER: Split Verse ---
// We will simply split by the middle space to make it fair.
// Or we can be smarter: if there is a common separator word like "لأن" or "كي", we use it?
// For consistency, let's stick to the middle split, but maybe prioritize a split that leaves at least 3 words on start.

export const splitVerse = (full: string): { start: string, end: string } => {
    const words = full.trim().split(' ');

    if (words.length <= 1) return { start: full, end: '...' };

    // Default split index (middle, heavily weighted to first half being slightly longer for context)
    let splitIndex = Math.ceil(words.length * 0.55);

    // Hard clamps
    if (words.length > 6 && splitIndex < 4) splitIndex = 4;

    // Simple logic logic

    const start = words.slice(0, splitIndex).join(' ');
    const end = words.slice(splitIndex).join(' ');

    return { start, end };
};
