// Helper to split a proverb intelligently
// Most Egyptian proverbs have a natural pause or are composed of two rhyming parts.
// We will use a simple heuristic: Split at the middle space, but prioritize certain connector words.

export const splitProverb = (full: string): { start: string, end: string } => {
    const words = full.trim().split(' ');

    // Safety check just in case
    if (words.length <= 1) return { start: full, end: '...' };

    let splitIndex = Math.ceil(words.length / 2);

    // Context-aware splitting for some patterns
    // e.g. "يا واخد القرد على ماله" (5 words) -> "يا واخد القرد" / "على ماله"
    // e.g. "اللي اختشوا ماتوا" (3 words) -> "اللي اختشوا" / "ماتوا"

    // If very short (3 words), split after the second word usually makes sense for "subject verb object"
    // "اللي اختشوا" (start) "ماتوا" (end)
    if (words.length === 3) {
        splitIndex = 2;
    }

    // If long, try to find the "middle" but lean towards the first part being slightly longer to give context.
    const start = words.slice(0, splitIndex).join(' ');
    const end = words.slice(splitIndex).join(' ');

    return { start, end };
};
